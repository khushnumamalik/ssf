function closeCouponPopup() {
  document.getElementById('couponPopup').style.display = 'none';
  document.getElementById('couponResult').textContent = '';
  document.getElementById('couponInput').value = '';
  document.getElementById('mobileInput').value = '';
  document.getElementById('continueBtn').disabled = true;
  document.getElementById('continueBtn').classList.remove('enabled');
}
function fillCoupon(code) {
  document.getElementById('couponInput').value = code;
  applyCoupon();
}
function applyCoupon() {
  const code = document.getElementById('couponInput').value.trim().toUpperCase();
  const result = document.getElementById('couponResult');
  if (code === 'SAVE10') {
    document.getElementById('orderDiscount').textContent = '-₹100';
    document.getElementById('orderTotal').textContent = '₹899';
    result.textContent = 'Coupon applied! You get 10% OFF.';
    result.style.color = 'green';
  } else if (code === 'FIRSTBUY') {
    document.getElementById('orderDiscount').textContent = '-₹150';
    document.getElementById('orderTotal').textContent = '₹849';
    result.textContent = 'First order coupon applied! ₹150 OFF.';
    result.style.color = 'green';
  } else if (code === 'FREESHIP') {
    document.getElementById('orderDiscount').textContent = '-₹50';
    document.getElementById('orderTotal').textContent = '₹949';
    result.textContent = 'Free shipping coupon applied! ₹50 OFF.';
    result.style.color = 'green';
  } else if (code === '') {
    result.textContent = 'Please enter a coupon code.';
    result.style.color = 'red';
  } else {
    document.getElementById('orderDiscount').textContent = '-₹0';
    document.getElementById('orderTotal').textContent = '₹999';
    result.textContent = 'Invalid coupon code.';
    result.style.color = 'red';
  }
}
